package variables

fun main(){
    //Constantes
    val constante = 10
    println(constante)

    //Variable
    var varia = "cadena"
    println(varia)

    /*
    * Las variables y constantes no necesitas explicitamente indicar su tipo de dato
    * sin embargo, si se puede utilizar esta notacion para especificarlo:
    */

    var otraVar:Int = 100
    println(otraVar)

}